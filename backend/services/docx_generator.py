def _white_run(cell, text, size, bold=True, align=None):
    cell.paragraphs[0].text = ""
    if align is not None:
        cell.paragraphs[0].alignment = align
    run = cell.paragraphs[0].add_run(text or "")
    run.bold = bold
    run.font.size = Pt(size)
    run.font.color.rgb = WHITE


def _build_scorecard_body(doc, sc):
    """Append a one-page ScorQ scorecard rendered as python-docx tables.

    `sc` is a dict: candidate_name, candidate_email, candidate_phone,
    overall_score, category_scores, matched_skills, highlights, ai_reasoning,
    job_title. No pdf2image / poppler — pure table layout."""
    category_scores = sc.get("category_scores") or {}

    # ── Navy header: ScorQ branding (L) + CANDIDATE SCORECARD (R) ──
    head = doc.add_table(rows=1, cols=2)
    _set_table_fixed_layout(head, [10.0, 7.0])
    hl, hr = head.rows[0].cells
    _set_cell_background(hl, "1A2744")
    _set_cell_background(hr, "1A2744")
    hl.paragraphs[0].text = ""
    r1 = hl.paragraphs[0].add_run("Scor")
    r1.bold = True; r1.font.size = Pt(16); r1.font.color.rgb = WHITE
    r2 = hl.paragraphs[0].add_run("Q")
    r2.bold = True; r2.font.size = Pt(16); r2.font.color.rgb = GOLD
    r3 = hl.paragraphs[0].add_run("  by HYROI Solutions")
    r3.font.size = Pt(8); r3.font.color.rgb = RGBColor(0xB9, 0xC2, 0xCF)
    _white_run(hr, "CANDIDATE SCORECARD", 8, bold=True,
               align=WD_ALIGN_PARAGRAPH.RIGHT)

    doc.add_paragraph()

    # ── Candidate name + contacts ────────────────────────────────
    np = doc.add_paragraph()
    nrun = np.add_run(sc.get("candidate_name") or "Candidate")
    nrun.bold = True; nrun.font.size = Pt(17); nrun.font.color.rgb = NAVY
    contact_bits = [b for b in (sc.get("candidate_email"), sc.get("candidate_phone")) if b]
    if contact_bits:
        cpp = doc.add_paragraph()
        crun = cpp.add_run("  ·  ".join(contact_bits))
        crun.font.size = Pt(9); crun.font.color.rgb = GRAY

    # ── Score boxes: 4 category cells, colored value + label ─────
    boxes = doc.add_table(rows=1, cols=4)
    _set_table_fixed_layout(boxes, [4.25, 4.25, 4.25, 4.25])
    for cell, (full, short, key, color, fill) in zip(boxes.rows[0].cells, _SC_CATS):
        score, _ = _sc_score(category_scores, key)
        _set_cell_background(cell, fill)
        cell.paragraphs[0].text = ""
        cell.paragraphs[0].alignment = WD_ALIGN_PARAGRAPH.CENTER
        lr = cell.paragraphs[0].add_run(short)
        lr.bold = True; lr.font.size = Pt(8); lr.font.color.rgb = color
        vp = cell.add_paragraph()
        vp.alignment = WD_ALIGN_PARAGRAPH.CENTER
        vr = vp.add_run(_pct(score))
        vr.bold = True; vr.font.size = Pt(15)
        vr.font.color.rgb = color if score is not None else GRAY

    doc.add_paragraph()

    # ── Score breakdown: colored-header card per category ────────
    _section_heading(doc, "SCORE BREAKDOWN")
    for full, short, key, color, fill in _SC_CATS:
        score, reasoning = _sc_score(category_scores, key)
        t = doc.add_table(rows=1, cols=2)
        _set_table_fixed_layout(t, [13.0, 4.0])
        cname, cval = t.rows[0].cells
        _set_cell_background(cname, fill)
        _set_cell_background(cval, fill)
        cname.paragraphs[0].text = ""
        cr = cname.paragraphs[0].add_run(full)
        cr.bold = True; cr.font.size = Pt(10); cr.font.color.rgb = color
        cval.paragraphs[0].text = ""
        cval.paragraphs[0].alignment = WD_ALIGN_PARAGRAPH.RIGHT
        vr = cval.paragraphs[0].add_run(_pct(score))
        vr.bold = True; vr.font.size = Pt(11)
        vr.font.color.rgb = color if score is not None else GRAY
        if reasoning:
            rp = doc.add_paragraph()
            rp.paragraph_format.left_indent = Cm(0.2)
            rr = rp.add_run(reasoning[:280])
            rr.font.size = Pt(8.5); rr.font.color.rgb = RGBColor(0x4B, 0x55, 0x63)

    # ── Matched skills (indigo chips, wrapped into a grid) ───────
    matched = [str(s).strip() for s in (sc.get("matched_skills") or []) if str(s).strip()][:20]
    if matched:
        doc.add_paragraph()
        _section_heading(doc, "MATCHED SKILLS")
        cols = 4
        rows = (len(matched) + cols - 1) // cols
        grid = doc.add_table(rows=rows, cols=cols)
        _set_table_fixed_layout(grid, [4.25, 4.25, 4.25, 4.25])
        for idx, skill in enumerate(matched):
            cell = grid.rows[idx // cols].cells[idx % cols]
            _set_cell_background(cell, CHIP_FILL)
            cell.paragraphs[0].text = ""
            cell.paragraphs[0].alignment = WD_ALIGN_PARAGRAPH.CENTER
            sr = cell.paragraphs[0].add_run(skill)
            sr.font.size = Pt(8); sr.font.color.rgb = INDIGO
        # Blank trailing cells stay unshaded (no fill applied).

    # ── Highlights (card) ────────────────────────────────────────
    highlights = sc.get("highlights") or []
    if highlights:
        doc.add_paragraph()
        _section_heading(doc, "HIGHLIGHTS")
        box = doc.add_table(rows=1, cols=1)
        _set_table_fixed_layout(box, [17.0])
        bcell = box.rows[0].cells[0]
        _set_cell_background(bcell, CARD_FILL)
        bcell.paragraphs[0].text = ""
        for i, h in enumerate(highlights[:5]):
            p = bcell.paragraphs[0] if i == 0 else bcell.add_paragraph()
            run = p.add_run(f"•  {str(h)[:200]}")
            run.font.size = Pt(9); run.font.color.rgb = RGBColor(0x37, 0x41, 0x51)

    # ── AI assessment (amber box) ────────────────────────────────
    ai = sc.get("ai_reasoning")
    if ai:
        doc.add_paragraph()
        box = doc.add_table(rows=1, cols=1)
        _set_table_fixed_layout(box, [17.0])
        bcell = box.rows[0].cells[0]
        _set_cell_background(bcell, AI_FILL)
        bcell.paragraphs[0].text = ""
        lab = bcell.paragraphs[0].add_run("AI ASSESSMENT")
        lab.bold = True; lab.font.size = Pt(9); lab.font.color.rgb = AI_LABEL
        tp = bcell.add_paragraph()
        tr = tp.add_run(str(ai)[:1200])
        tr.font.size = Pt(9); tr.font.color.rgb = AI_TEXT

    return doc


def create_combined_docx(
    structured_data: dict,
    scorecard: dict,
    company_name: str = "HYROI Solutions",
    mask_contacts: bool = False,
    logo_bytes: bytes = None,
    company_tagline: str = None,
    include_header: bool = True,
    include_footer: bool = True,
) -> bytes:
    """Combined DOCX: crafted resume pages followed by the scorecard rendered as
    a structured table page (last section). The resume pages follow the
    include_header / include_footer toggles; the scorecard section always keeps
    its own ScorQ footer."""
    from docx.enum.section import WD_SECTION

    doc = Document()
    _setup_page(doc)

    # ── Resume section ───────────────────────────────────────────
    _build_resume_body(
        doc, structured_data, mask_contacts=mask_contacts, logo_bytes=logo_bytes,
        company_name=company_name, company_tagline=company_tagline,
        include_header=include_header,
    )
    if include_footer:
        _set_section_footer(
            doc.sections[-1],
            f"Generated by RecruitCraft · {company_name} · Confidential",
        )

    # ── Scorecard on a new section/page with its own ScorQ footer ─
    doc.add_section(WD_SECTION.NEW_PAGE)
    _build_scorecard_body(doc, scorecard or {})
    _set_section_footer(
        doc.sections[-1], "Generated by ScorQ · HYROI Solutions"
    )

    buffer = io.BytesIO()
    doc.save(buffer)
    buffer.seek(0)
    return buffer.getvalue()
